"use client";

import { use, useEffect, useState } from "react";

function severityEmoji(text: string) {
  const t = text.toLowerCase();
  if (t.includes("blocked") || t.includes("could not") || t.includes("fail") || t.includes("unclear")) return "ðŸ”´";
  if (t.includes("scroll") || t.includes("hesitated") || t.includes("not obvious")) return "ðŸŸ¡";
  return "ðŸŸ¢";
}

export default function ResultPage({ params }: { params: Promise<{ id: string }> }) {
  const { id } = use(params);
  const [report, setReport] = useState<any>(null);

  useEffect(() => {
    const raw = sessionStorage.getItem(`report_${id}`);
    if (raw) setReport(JSON.parse(raw));
  }, [id]);

  if (!report) {
    return (
      <main className="min-h-screen bg-white text-black">
        <div className="max-w-2xl mx-auto p-6">
          <h1 className="text-2xl font-bold">AI Test Report</h1>
          <p className="mt-2 text-gray-600">
            No report found. Go back and run a simulation.
          </p>
        </div>
      </main>
    );
  }

  return (
    <main className="min-h-screen bg-white text-black">
      <div className="max-w-2xl mx-auto p-6">
        <h1 className="text-2xl font-bold">AI UX Report</h1>
        <p className="mt-2 text-gray-600">
          Mode: <span className="font-medium">{report.mode}</span>
        </p>

        <div className="mt-6 rounded border p-4">
          <div className="text-sm text-gray-600">UX Confidence Score</div>
          <div className="text-4xl font-bold">{report.confidence_score}</div>
        </div>

        <div className="mt-6">
          <h2 className="text-lg font-semibold">UX Frictions</h2>
          <ul className="mt-3 space-y-2">
            {(report.ux_frictions || []).map((x: string, i: number) => (
              <li key={i} className="rounded border p-3">
                <div className="text-sm text-gray-600">Severity: {severityEmoji(x)}</div>
                <div className="mt-1">{x}</div>
              </li>
            ))}
          </ul>
        </div>

        <div className="mt-6">
  <h2 className="text-lg font-semibold">Simulation steps</h2>
  <ul className="mt-3 space-y-2">
    {(report.steps || []).map((s: any, i: number) => (
      <li key={i} className="rounded border p-3">
        <div className="font-medium">{i + 1}. {s.step}</div>
        <div className="text-sm text-gray-600 mt-1">{s.note}</div>
      </li>
    ))}
  </ul>
</div>

        <div className="mt-8 text-sm text-gray-500">
          MVP note: this report is generated by a simulation runner. Next weâ€™ll add screenshots + step-by-step flow.
        </div>
      </div>
    </main>
  );
}